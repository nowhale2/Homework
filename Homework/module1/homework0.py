string1 = 'это моя строка'
string2 = 'это новая строка'

print(string1[0])               # Вывод первого символа
print(string1[-1])              # Вывод последнего символа
print(string1[2:5:1])           # Вывод с третьего по пятый символ
print(string1[::-1])            # Вывод строки наоборот
print(len(string1))             # Вывод длины строки
print(string2 + ' ' + string1)  # Вывод сложения строк
